function App() {
  const [start, setStart] = useState<[number, number]>();
  const [end, setEnd] = useState<[number, number]>();

  const handleClick = (row: number, col: number) => {
    if (!start) {
      setStart([row, col]);
    } else {
      setEnd([row, col]);
    }
  };

  return (
    <div className="App">
      <div className="maze-container">
        {MAZE.map((row, rowIndex) => (
          <div className="row" key={rowIndex}>
            {row.map((_, colIndex) => (
              <Cell
                isStart={
                  start ? rowIndex === start[0] && colIndex === start[1] : false
                }
                isEnd={end ? rowIndex === end[0] && colIndex === end[1] : false}
                key={colIndex}
                handleClick={() => handleClick(rowIndex, colIndex)}
              />
            ))}
          </div>
        ))}
        <button>solve</button>
      </div>
    </div>
  );
}

export default App;
